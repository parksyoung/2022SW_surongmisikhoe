public class s_rst {
    private String dong;
    private String name;


    public s_rst() {
    }

    public String getDong() {
        return dong;
    }

    public void setDong(String dong) {
        this.dong = dong;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }



    public s_rst(String dong, String name) {
        this.dong = dong;
        this.name = name;

    }

}



